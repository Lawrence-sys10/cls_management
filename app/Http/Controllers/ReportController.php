    /**
     * Display the main reports index page.
     */
    public function index(Request $request): View
    {
        $search = $request->input('search');
        $type = $request->input('type');

        $query = Report::with('generatedBy');

        if ($search) {
            $query->where('name', 'like', "%{$search}%")
                  ->orWhere('type', 'like', "%{$search}%");
        }

        if ($type) {
            $query->where('type', $type);
        }

        $reports = $query->latest()->paginate(15);

        // Statistics for the dashboard
        $reportsThisMonth = Report::whereMonth('created_at', now()->month)
            ->whereYear('created_at', now()->year)
            ->count();

        // Count all reports as ready for export since we don't have status column
        $readyForExport = $reports->count();

        return view('reports.index', compact(
            'reports',
            'reportsThisMonth',
            'readyForExport'
        ));
    }

    /**
     * Export reports list (for the export button functionality)
     */
    public function export(Request $request)
    {
        $search = $request->input('search');
        $type = $request->input('type');
        $format = $request->input('format', 'csv');
        
        // Get filtered reports based on the request parameters
        $query = Report::query();
        
        if ($search) {
            $query->where('name', 'like', "%{$search}%")
                  ->orWhere('type', 'like', "%{$search}%");
        }
        
        if ($type) {
            $query->where('type', $type);
        }
        
        $reports = $query->get();
        
        // Generate filename with timestamp
        $filename = 'reports_export_' . now()->format('Y_m_d_His') . '.' . $format;
        
        // Handle different export formats
        switch ($format) {
            case 'pdf':
                return $this->exportPdf($reports, $filename);
                
            case 'excel':
            case 'csv':
            default:
                return $this->exportCsv($reports, $filename);
        }
    }

    /**
     * Export to PDF
     */
    private function exportPdf($reports, $filename)
    {
        try {
            $pdf = PDF::loadView('reports.exports.pdf', compact('reports'));
            return $pdf->download($filename);
        } catch (\Exception $e) {
            // Fallback to CSV if PDF generation fails
            return $this->exportCsv($reports, str_replace('.pdf', '.csv', $filename));
        }
    }

    /**
     * Export to CSV
     */
    private function exportCsv($reports, $filename)
    {
        $headers = [
            'Content-Type' => 'text/csv',
            'Content-Disposition' => "attachment; filename=\"$filename\"",
        ];

        $callback = function() use ($reports) {
            $file = fopen('php://output', 'w');
            
            // Add BOM for UTF-8
            fwrite($file, "\xEF\xBB\xBF");
            
            // Add CSV headers - only available columns
            fputcsv($file, [
                'ID', 'Report Name', 'Type', 'Generated By', 'Created At', 'Updated At'
            ]);
            
            // Add data rows - only available columns
            foreach ($reports as $report) {
                fputcsv($file, [
                    $report->id,
                    $report->name ?? 'Unnamed Report',
                    $report->type ?? 'general',
                    $report->generated_by ?? 'System',
                    $report->created_at?->format('Y-m-d H:i:s') ?? '',
                    $report->updated_at?->format('Y-m-d H:i:s') ?? '',
                ]);
            }
            
            fclose($file);
        };

        return response()->stream($callback, 200, $headers);
    }

